/**
 * Power system — lightning rods capture storm energy and distribute it.
 *
 * Power flows from lightning rods to connected buildings/units via BFS.
 * Buildings within a rod's protection radius receive power.
 * Units can charge when near a powered building.
 *
 * Storm intensity affects rod output (fluctuates over time).
 */

import { config } from "../../config";
import type { BuildingEntity } from "../ecs/types";
import { buildings, lightningRods, units } from "../ecs/koota/compat";
import { getCurrentWeather } from "./weatherSystem";
import { getWeatherModifiers } from "./weatherEffects";

const powerCfg = config.power;

/** How far power reaches from a lightning rod */
const DEFAULT_POWER_RADIUS = powerCfg.defaultPowerRadius;

/** Storm intensity oscillates — affects all rod output */
let stormIntensity = 1.0;
let stormPhase = 0;

/**
 * Update storm intensity. Called each sim tick.
 * The storm fluctuates with slow waves, occasionally surging.
 */
function updateStormIntensity(tick: number) {
	stormPhase = tick * powerCfg.stormPhaseRate;
	// Base oscillation (slow sine wave)
	const base =
		powerCfg.stormBaseOscillationMin +
		powerCfg.stormBaseOscillationAmplitude *
			Math.sin(stormPhase * powerCfg.stormBaseFrequency);
	// Occasional surges
	const surge =
		Math.max(
			0,
			Math.sin(
				stormPhase * powerCfg.stormSurgeFrequency +
					powerCfg.stormSurgePhaseOffset,
			),
		) * powerCfg.stormSurgeAmplitude;
	stormIntensity = Math.min(powerCfg.stormMaxIntensity, base + surge);
}

export function getStormIntensity(): number {
	return stormIntensity;
}

/** Restore storm intensity from a save file. */
export function setStormIntensity(intensity: number): void {
	stormIntensity = intensity;
}

/**
 * Calculate total power generated by all lightning rods.
 * Storm weather multiplies rod output via weatherEffects.lightningChanceMult.
 */
function getTotalPowerGeneration(): number {
	const weatherMult = getWeatherModifiers(getCurrentWeather()).lightningChanceMult;
	let total = 0;
	for (const rod of lightningRods) {
		const output = rod.lightningRod.rodCapacity * stormIntensity * weatherMult;
		rod.lightningRod.currentOutput = output;
		total += output;
	}
	return total;
}

/**
 * Calculate total power demanded by all buildings and units.
 */
function getTotalPowerDemand(): number {
	let demand = 0;
	for (const building of buildings) {
		if (building.building.operational) {
			demand += getBuildingPowerDemand(building);
		}
	}
	for (const unit of units) {
		// Skip fabrication units — their demand is counted via building component
		if (unit.unit.type === "fabrication_unit") continue;
		demand += getUnitPowerDemand(unit);
	}
	return demand;
}

function getBuildingPowerDemand(entity: BuildingEntity): number {
	switch (entity.building.type) {
		case "fabrication_unit":
			return powerCfg.fabricationUnitDemand;
		case "lightning_rod":
			return 0; // generates, doesn't consume
		case "miner":
			return powerCfg.minerDemand;
		default:
			return powerCfg.defaultBuildingDemand;
	}
}

function getUnitPowerDemand(entity: {
	navigation?: { moving: boolean };
}): number {
	const baseDemand = powerCfg.unitBaseDemand;
	const movingBonus = entity.navigation?.moving ? powerCfg.unitMovingBonus : 0;
	return baseDemand + movingBonus;
}

/**
 * Distribute power: buildings within rod range get powered.
 * If total generation < total demand, some buildings lose power.
 */
function distributePower() {
	// First, mark all buildings as unpowered
	for (const building of buildings) {
		if (building.building.type !== "lightning_rod") {
			building.building.powered = false;
		}
	}

	// For each rod, power nearby buildings
	for (const rod of lightningRods) {
		const radius = rod.lightningRod.protectionRadius || DEFAULT_POWER_RADIUS;
		const rx = rod.worldPosition.x;
		const rz = rod.worldPosition.z;

		for (const building of buildings) {
			if (building.building.type === "lightning_rod") continue;

			const dx = building.worldPosition.x - rx;
			const dz = building.worldPosition.z - rz;
			const dist = Math.sqrt(dx * dx + dz * dz);

			if (dist <= radius) {
				building.building.powered = true;
				building.building.operational = true;
			}
		}
	}
}

export interface PowerSnapshot {
	totalGeneration: number;
	totalDemand: number;
	stormIntensity: number;
	rodCount: number;
	poweredBuildingCount: number;
}

let lastPowerSnapshot: PowerSnapshot = {
	totalGeneration: 0,
	totalDemand: 0,
	stormIntensity: 1.0,
	rodCount: 0,
	poweredBuildingCount: 0,
};

export function getPowerSnapshot(): PowerSnapshot {
	return lastPowerSnapshot;
}

/**
 * Run power system. Called once per sim tick.
 */
export function powerSystem(tick: number) {
	updateStormIntensity(tick);
	const generation = getTotalPowerGeneration();
	const demand = getTotalPowerDemand();
	distributePower();

	let poweredCount = 0;
	for (const building of buildings) {
		if (building.building.powered) poweredCount++;
	}

	lastPowerSnapshot = {
		totalGeneration: Math.round(generation * 10) / 10,
		totalDemand: Math.round(demand * 10) / 10,
		stormIntensity: Math.round(stormIntensity * 100) / 100,
		rodCount: Array.from(lightningRods).length,
		poweredBuildingCount: poweredCount,
	};
}
